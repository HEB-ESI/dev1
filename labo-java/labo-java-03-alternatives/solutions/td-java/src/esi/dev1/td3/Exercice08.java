package esi.dev1.td3;

public class Exercice08 {

    public static void main(String[] args) {
        System.out.print("9.99 == 10.0\t\t\t\t\t");
        System.out.println(9.99 == 10.0);
        
        System.out.print("9.999999999999999999999999 == 10.0\t\t");
        System.out.println(9.999999999999999999999999 == 10.0);
        
        System.out.print("9.999999999999999999999999 == 10.00000000000001\t");
        System.out.println(9.999999999999999999999999 == 10.00000000000001);
        
        System.out.print("true\t\t\t\t\t\t");
        System.out.println(true);
        
        System.out.print("false\t\t\t\t\t\t");
        System.out.println(false);
        
        System.out.print("!true\t\t\t\t\t\t");
        System.out.println(!true);
        
        System.out.print("!false\t\t\t\t\t\t");
        System.out.println(!false);
        
        System.out.print("true && true\t\t\t\t\t");
        System.out.println(true && true);
        
        System.out.print("true && false\t\t\t\t\t");
        System.out.println(true && false);
        
        System.out.print("false && true\t\t\t\t\t");
        System.out.println(false && true);
        
        System.out.print("false && false\t\t\t\t\t");
        System.out.println(false && false);
        
        System.out.print("true || true\t\t\t\t\t");
        System.out.println(true || true);
        
        System.out.print("true || false\t\t\t\t\t");
        System.out.println(true || false);
        
        System.out.print("false || true\t\t\t\t\t");
        System.out.println(false || true);
        
        System.out.print("false || false\t\t\t\t\t");
        System.out.println(false || false);
    }
}
